/*
* \brief Calcula la resta entre dos números
* \param int primer número
* \param int segundo número
* \return el resultado de la resta
*
*/



float Resta(float, float);



/*
* \brief Calcula la division entre dos números
* \param int primer número
* \param int segundo número
* \return el resultado de la división
*
*/

float Division(float, float);


/*
* \brief Calcula la multiplicacion entre dos números
* \param int primer número
* \param int segundo número
* \return el resultado de la multiplicación
*
*/

float Multiplicacion(float, float);

/*
* \brief Calcula el factorial de un número
* \param int número ingresado
* \return el resultado del factorial
*
*/

float Factorial(float);



/*
* \brief Calcula el promedio entre dos números
* \param int número ingresado1
* \param int número ingresado2
* \return el resultado del promedio
*
*/



float SacarPromedio(float, float);


